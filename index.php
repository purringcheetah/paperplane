                <h1><a id="overview"><img src="images/title-overview.png"/><span>Overview</span></a></h1>
                <p>
                    PaperPlane is a video game where you fly a paper airplane through typical childhood memories. You give life back to these memories
                    by flying close to them, through them or around them.
                </p>
                <p>
                    Enabling those memories give access to little sketches which put some movement to the landscape. Also, each one of them is can be viewed
                    in a polaroid pictures book inside the tree house.
                </p>
                <p>
                    PaperPlane won the student first prize (ex-aequo) at the Milthon Awards 2010.
                </p>
                <p>
                    PaperPlane got the <a href="http://igf.com/2011/01/2011_independent_games_festiva_12.html">IGF 2011 Student Showcase</a> prize.
                </p>
